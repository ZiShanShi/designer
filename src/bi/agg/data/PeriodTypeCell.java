package bi.agg.data;

public class PeriodTypeCell {

	private String name;
	private String shortName;
	
	public PeriodTypeCell(String name, String shortName) {
		this.name = name;
		this.shortName = shortName;
	}

	public String getName() {
		return name;
	}

	public String getShortName() {
		return shortName;
	}
	
}
